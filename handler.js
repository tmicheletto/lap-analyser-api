const sessionsUpdate = require('./sessions-update.js');
const sessionsDelete = require('./sessions-delete.js');

module.exports.create = (event, context, callback) => {
  sessionsCreate(event, (error, result) => {
    const response = {
      statusCode: 200,
      headers: {
        "Access-Control-Allow-Origin" : "*"
      },
      body: JSON.stringify(result),
    };

    context.succeed(response);
  });
};

